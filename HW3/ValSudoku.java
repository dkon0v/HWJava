// Отвалидировать доску судоку

public class ValSudoku {
    public static boolean ValidSudoku(char[][] board) {
        // проверяем строки и столбцы
        for (int i = 0; i < 9; i++) {
            boolean[] row = new boolean[9];
            boolean[] col = new boolean[9];
            for (int j = 0; j < 9; j++) {
                if (board[i][j] != '.') {
                    int num1 = board[i][j] - '1'; // получаем числовое значение цифры
                    if (row[num1]) return false; // если такое число уже встречалось в строке, возвращаем false
                    row[num1] = true; // иначе отмечаем, что это число уже встречалось в строке
                }
                if (board[j][i] != '.') {
                    int num2 = board[j][i] - '1'; // получаем числовое значение цифры
                    if (col[num2]) return false; // если такое число уже встречалось в столбце, возвращаем false
                    col[num2] = true; // иначе отмечаем, что это число уже встречалось в столбце
                }
            }
        }
        
        // проверяем блоки 3x3
        for (int i = 0; i < 9; i += 3) {
            for (int j = 0; j < 9; j += 3) {
                boolean[] block = new boolean[9];
                for (int k = 0; k < 9; k++) {
                    int row = i + k / 3;
                    int col = j + k % 3;
                    if (board[row][col] != '.') {
                        int num = board[row][col] - '1'; // получаем числовое значение цифры
                        if (block[num]) return false; // если такое число уже встречалось в блоке, возвращаем false
                        block[num] = true; // иначе отмечаем, что это число уже встречалось в блоке
                    }
                }
            }
        }
        
        // если все проверки пройдены успешно, возвращаем true
        return true;
    }    
}

// Этот метод проходит по всем строкам и столбцам доски, а также по блокам 3x3, и проверяет, что каждая цифра от 1 до 9 встречается только один раз в каждой строке, столбце и блоке.
// Если хоть одно условие не выполняется, метод возвращает false. 
// Если все проверки пройдены успешно, метод возвращает true.